    <h4><pre>
    我們的服務主要針對兒童及青少年的發展而提供服務。當中包括不同模式的訓練及證書課程。
    本機會更與不同的非牟利機構合作為服務使用者提供貼身的服務以達致活動的目標。
    ​
    我們深信年輕人潛力無限，創意非凡，但因為沈重的學業，往往忽略了生活平衡。
    我們希望通過歷奇體驗和專業導師設計的課程，讓年輕人自我探索，發揮潛能。
    機構提倡“快樂成長”， 通過教室內外的體驗和專家分享，激發學生的求知慾，實踐和探索能力。
    同時也教導學生在陌生環境下克服困難，建立自信。
     
    我們的成員包括註冊社工、專業輔導員、歷奇導師、龍舟教練、體適能教練、註冊護士及紀律部隊等。
